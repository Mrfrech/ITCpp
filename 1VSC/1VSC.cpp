#include <iostream> // <iostream is the pre-built libaray used for print our program in the console

int main() { //This main() function is the entry point of our program
             //An entry point is a program where the execution of a program begins
    std::cout << "hello world" << std::endl; //std::cout is a part of <iostream> library and its shows output to the console
    std::cout << "number1" << std::endl;
    std::cout << "number2";
    std::cout << "number3" << std::endl;     //std::endl is for adding new line after printing "hello world" 
    return 0;
}
